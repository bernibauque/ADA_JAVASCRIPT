// Ejemplo: Verificar si una persona es mayor de edad

// Paso 1: Declaramosla variable de edad de la persona 
let edad = 18;

// Paso 2: Usamos el operador ternario para determinar si es mayor de edad
let mensaje = edad >= 20 ? "Mayor de edad" : "Menor de edad";
// La condicion es: Edad es mayor o igual a 18?
// Si es verdadera, el resultado sera "Mayor de edad"
// Si es falsa, el resultado sera "Menor de edad"

// Paso 3: mostramos por consola
console.log(mensaje);

// Explicacion :
// El operador ternario funciona como un if pero en una sola linea
// Estructura: condicion ? valor_si_verdadero : valor_si_falso
// En este caso:
// - condicion: edad >= 18
// - valor_si_verdadero: "Mayor de edad"
// - valor_si_falso: "Menor de edad"
