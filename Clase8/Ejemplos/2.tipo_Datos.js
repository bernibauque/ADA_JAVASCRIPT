// Definimos diferentes tipos de datos
let numero = 42;                 // Número
let texto = "Hola Mundo";        // Cadena de texto
let booleano = true;             // Booleano (verdadero o falso)
let indefinido;                  // Variable no inicializada (Indefinido)
let nulo = null;                 // Valor nulo (vacío)
let objeto = { nombre: "Luis", edad: 30 }; // Objeto con propiedades

// Mostramos los valores y explicamos su tipo
console.log("Número:", numero);            // Esto es un número
console.log("Texto:", texto);              // Esto es una cadena de texto
console.log("Booleano:", booleano);        // Esto es un booleano (verdadero o falso)
console.log("Indefinido:", indefinido);    // Esto es indefinido (la variable no tiene un valor)
console.log("Nulo:", nulo);                // Esto es nulo (representa un valor vacío)
console.log("Objeto:", objeto);            // Esto es un objeto (con nombre y edad)

