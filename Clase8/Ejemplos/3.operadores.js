// Operadores aritméticos
let a = 10;
let b = 3;
console.log(a + b); // Suma: 13
console.log(a - b); // Resta: 7
console.log(a * b); // Multiplicación: 30
console.log(a / b); // División: 3.3333...
console.log(a % b); // Módulo (resto de la división): 1

// Operadores de comparación
console.log(a > b);   // true - a es mayor que b
console.log(a < b);   // false - a no es menor que b
console.log(a === 10); // true - a es igual a 10

// Operadores lógicos
console.log(true && false);
// && (AND): Devuelve true solo si ambos valores son true. 
//Aquí es false porque uno de los valores es false.

console.log(true || false);
// || (OR): Devuelve true si al menos uno de los valores es true. 
//Aquí es true porque al menos uno es true.

console.log(!true);
// ! (NOT): Invierte el valor booleano.
//Aquí devuelve false porque invierte el true.
